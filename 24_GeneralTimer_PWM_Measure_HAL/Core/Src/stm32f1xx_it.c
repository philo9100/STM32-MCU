uint8_t risingEdgeCount = 0;
uint16_t t = 0;
/* TIM4的中断回调函数，输入捕获的回调函数 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM4)
  {
    if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
    {
      /* 如果是第1个上升沿，则清零计数器，让计数器从0开始计时 */
      risingEdgeCount++;
      if (risingEdgeCount == 1)
      {
        // TIM4->CNT = 0; /* 计数器清0 */
        /* 使用HAL库的宏 */
        __HAL_TIM_SetCounter(htim, 0);
      }
      else if (risingEdgeCount == 2)
      {
        /* 读取捕获寄存器的值，就是周期 */
        // t = TIM4->CCR1;
        /* 使用HAL库的宏 */
        t = __HAL_TIM_GetCompare(htim, TIM_CHANNEL_1);

        /* 上升沿的计数清0重新计数 */
        risingEdgeCount = 0;
      }
    }
  }
}


