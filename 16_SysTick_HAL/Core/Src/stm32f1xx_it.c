/* SysTick 中断服务函数中会执行这个函数来处理中断 */
void HAL_IncTick(void)
{
  /*
  下面两个变量专门用于处理 Systick Timer 中断中用来记录滴答次数的变量
  uwTick：这是一个全局变量，用于记录中断产生的的滴答总次数
  uwTickFreq：通常表示每次中断用来记录的滴答次数，默认是 1 KHz，实际值是 1
  
  uwTickFreq 的实际值为 1，每经过1ms产生一次中断调用一次 HAL_IncTick() 函数
  则uwTick记录 1K 个滴答次数，需要触发1000次中断，即需要1s，因此 uwTickFreq 默认位 1KHz 
  */
  uwTick += uwTickFreq;

  /* 
  uwTickFreq 配置为 1KHz，每 1ms 触发一个中断，uwTick记录 1 个滴答次数
  1K ms（1s）触发 1K 个中断，uwTick记录 1K 个滴答次数
  如果让LED灯每经过1s（1K个滴答）翻转一次，可以通过判断uwTick的值来控制LED灯的翻转
  */
  if (uwTick % 1000 == 0)
  {
    printf("%d\r\n", uwTick);
    HAL_GPIO_TogglePin(LED_1_GPIO_Port, LED_1_Pin);
  }
}

