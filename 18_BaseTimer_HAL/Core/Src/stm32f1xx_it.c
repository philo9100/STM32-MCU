/* TIM Update event */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  /* NOTE : This function should not be modified, when the callback is needed,
            the HAL_TIM_PeriodElapsedCallback could be implemented in the user file
   */
  /* 使用HAL库的中断回调函数会由HAL库自动清除中断挂起标志位，不需要自己手动清除
    在 3947 行 TIM Update event 后直接就是进行清除的逻辑
    首先 3948 行进行 FLAG 位的判断
    将 Tim6 的 SR 寄存器值与 TIM_SR_UIF 位进行与操作并判断是否处于中断挂起，如果处于中断挂起就执行 3950 行
    在 3950 行进行判断
    将 Tim6 的 DIER 寄存器值与 TIM_SR_UIE 位进行与操作并判断是否开启更新中断使能，如果开启了更新中断使能就执行 3952 行
    在 3952 行进行了清除操作
    将 SR 寄存器中的值写为0，清除中断挂起标志位
  */
  if(htim == &htim6)
  {
    HAL_GPIO_TogglePin(LED_1_GPIO_Port, LED_1_Pin);
  }

}


