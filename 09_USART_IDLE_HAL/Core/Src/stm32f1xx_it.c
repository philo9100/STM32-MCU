extern uint8_t RXBUFFER;

/**
 * @brief 当调用HAL_UARTEx_ReceiveToIdle_IT()后，当接收到空闲帧时会产生中断并调用这个回调函数
 * @param huart 
 * @param Size 
 * 
 * 找函数使用方式的方法，通过HAL_UARTEx_ReceiveToIdle_IT()函数找到其所在文件
 * 在该文件中搜索所有与HAL_UARTEx_ReceiveToIdle_IT() 相关的函数的使用，其中一定包含该函数的回调函数HAL_UARTEx_RxEventCallback()
 * 
 */
void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
  if(huart->Instance == USART1) // 判断是否是串口1发生的中断
  {
    HAL_UART_Transmit(&huart1, RXBUFFER, Size, 1000);
    // 中断处理完后需要再次调用才可以接收下一次的中断
    HAL_UARTEx_ReceiveToIdle_IT(&huart1, RXBUFFER, 100);
  }
}



